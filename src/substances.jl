# creating a structure to van der Waals gases

struct vdWGas
    
    nome::String
    
    Tc::sysT{Float64,EX}
    
    Pc::sysP{Float64,EX}
    
    vc::vAmt{Float64,EX,MA}
        
    M::mAmt{Float64,EX,MO}
    
    ϕ::Float64
    
end

name(gas::vdWGas) = gas.nome

Pc(gas::vdWGas) = gas.Pc

Tc(gas::vdWGas) = gas.Tc

vc(gas::vdWGas) = gas.vc

M(gas::vdWGas) = gas.M

ϕ(gas::vdWGas) = gas.ϕ

R_gas(gas::vdWGas) = 8*(gas.Pc/gas.Tc)*(gas.vc/3)

α(gas::vdWGas) = (27/64)*(R_gas(gas)^2)*(gas.Tc^2)/gas.Pc

b(gas::vdWGas) = (R_gas(gas)/8)*gas.Tc/gas.Pc

export name

export Pc

export Tc

export vc  

export M

export ϕ

export R_gas

export α

export b

